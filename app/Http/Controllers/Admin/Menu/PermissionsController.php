     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $role = Role::find($id);
        $permissions = Permission::all();
        return view('admin.permission.editPermission',[
            'role'=>$role,
            'permissions'=>$permissions,
        ]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $setting_permissions = $request->get('permissions');
        $role = Role::find($id);
        $permissions = Permission::all();
        foreach ($permissions as $permission){
            $role->revokePermissionTo($permission->name);
        }
        foreach ($setting_permissions as $setting_permission){
            $role->givePermissionTo($setting_permission);
        }

        return redirect(route('admin.permission.index'))->with('message','權限已成功更新!');
        //
    }

